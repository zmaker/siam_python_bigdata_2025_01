testo = "quel ramo del lago di Como"
print(testo)
print( testo[2:6] ) #da 2 a 6
print( testo[:6] )  #dall'inizio a 6
print( testo[20:] )  #da 20 fino in fondo
print( testo[20:-2] )
print( testo[:-1] )

